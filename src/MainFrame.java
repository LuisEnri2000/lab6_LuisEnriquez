    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        try {
            String nombre = tf_nombre.getText();
            int duracion = (int) s_duracion.getValue();
            String categ = (String) cb_genero.getSelectedItem();
            String actores = tf_actores.getText();
            String director = tf_director.getText();
            String compa = tf_compania.getText();
            String idioma = (String) cb_idioma.getSelectedItem();
            boolean dob = rb_doblaje.isSelected();
            boolean sub = rb_subtitulos.isSelected();

            Pelicula p = new Pelicula(nombre,duracion,categ,actores,director,compa,idioma,dob,sub);
            aP.getlistaPeliculas().add(p);
            
            tf_nombre.setText("");
            s_duracion.setValue(120);
            tf_actores.setText("");
            tf_director.setText("");
            tf_compania.setText("");
            rb_doblaje.setSelected(false);
            rb_subtitulos.setSelected(false);
            
            JOptionPane.showMessageDialog(this, "Se agrego con exito! Pero no se han guardado los cambios");
            
            jd_crearPelicula.setVisible(false);
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Hubo un error al guardar!\nIntenta luego!");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

