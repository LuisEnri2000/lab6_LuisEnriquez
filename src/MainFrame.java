    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        aP.cargarArchivo();
        aS.cargarArchivo();
        
        File f = null;
        FileReader fr = null;
        BufferedReader br = null;
        try {
            f = new File("./usuarios.txt");
            fr = new FileReader(f);
            br = new BufferedReader(fr);
            boolean log = false;
            String l;
            
            while ((l = br.readLine()) != null) {
                String[] al = l.split(";"); 
                                
                if ((jf_user.getText()).equals(al[0]) && (jf_pass.getText()).equals(al[2])) {
                    log = true;
                }
            }
            
            if (log) {
                jd_pr.setModal(true);
                jd_pr.pack();
                jd_pr.setVisible(true);
                jf_user.setText("");
                jf_pass.setText("");
                                               
            }else{
                    JOptionPane.showMessageDialog(this, "Oops! Revisa los datos!");
            }
            br.close();
            fr.close();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Oops! Hubo un error!");
        }
        
            
    }//GEN-LAST:event_jButton1ActionPerformed

